namespace Weda.Template.Domain.Employees.Enums;

/// <summary>
/// Represents the department an employee belongs to.
/// </summary>
public enum Department
{
    /// <summary>
    /// Engineering department - software development and technical roles.
    /// </summary>
    Engineering = 0,

    /// <summary>
    /// Human Resources department - recruitment, benefits, employee relations.
    /// </summary>
    HumanResources = 1,

    /// <summary>
    /// Finance department - accounting, budgeting, financial planning.
    /// </summary>
    Finance = 2,

    /// <summary>
    /// Marketing department - brand management, advertising, communications.
    /// </summary>
    Marketing = 3,

    /// <summary>
    /// Sales department - customer acquisition, account management.
    /// </summary>
    Sales = 4,

    /// <summary>
    /// Operations department - logistics, supply chain, facilities.
    /// </summary>
    Operations = 5,
}
